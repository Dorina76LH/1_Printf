#include "ft_printf.h"

// ============================================================================
// ------------------------------- print_str ----------------------------------
// ============================================================================

// prints a string on the stdout and returns de count of printed chars

int	print_string(char *str)
{	
	// gestion des chaines null
	if (str == NULL)
		str = "(null)";
	// appel ft_putstr_fd
	ft_putstr_fd(str, 1);
	// retourner le nombre de caracteres imprimes
	return (ft_strlen(str));
}