#include "ft_printf.h"

// ============================================================================
// ----------------------------- print_number ---------------------------------
// ============================================================================

int	print_number(long n)
{
	int			count;
	const char	*symbols;

	count = 0;
	symbols = "0123456789";
	if (n < 0)
	{
		count += write(1, "-", 1);
		n = -n;
	}
	else if (n < 10)
		count += print_char(symbols[n]);
	else
	{
		count += print_number(n / 10);
		count += print_number(n % 10);
	}
	return (count);
}
